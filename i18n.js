let activeLocale = DEFAULT_LANG;
try {
  const stored = globalThis.window?.localStorage?.getItem(STORAGE_KEY);
  if (stored === "ko" || stored === "en") {
    activeLocale = stored;
  }
} catch {
  // localStorage unavailable (private mode, etc.) — fall through to default.
}

function currentLang() {
  return activeLocale;
}

function translate(key, lang = currentLang()) {
  const dict = translations[lang] || translations[DEFAULT_LANG];
  return dict[key] ?? translations[DEFAULT_LANG][key] ?? "";
}

function applyLanguage(lang) {
  activeLocale = (lang === "en" || lang === "ko") ? lang : DEFAULT_LANG;
  if (typeof document === "undefined") return;
  const dict = translations[activeLocale] || translations[DEFAULT_LANG];
  document.documentElement.lang = activeLocale;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (Object.prototype.hasOwnProperty.call(dict, key)) {
      element.textContent = dict[key];
    }
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.dataset.i18nAria;
    if (Object.prototype.hasOwnProperty.call(dict, key)) {
      element.setAttribute("aria-label", dict[key]);
    }
  });
  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const key = element.dataset.i18nAlt;
    if (Object.prototype.hasOwnProperty.call(dict, key)) {
      element.setAttribute("alt", dict[key]);
    }
  });
  document.querySelectorAll("[data-i18n-label]").forEach((element) => {
    const key = element.dataset.i18nLabel;
    if (Object.prototype.hasOwnProperty.call(dict, key)) {
      element.setAttribute("label", dict[key]);
    }
  });
  const toggle = document.querySelector("#lang-toggle");
  if (toggle) {
    toggle.textContent = lang === "ko" ? "EN" : "한국어";
    toggle.setAttribute("aria-pressed", lang === "en" ? "true" : "false");
  }
}

function setLanguage(lang) {
  const normalized = (lang === "en" || lang === "ko") ? lang : DEFAULT_LANG;
  activeLocale = normalized;
  try {
    globalThis.window?.localStorage?.setItem(STORAGE_KEY, activeLocale);
  } catch {
    // Ignore persistence failures; the session still gets the requested language.
  }
  applyLanguage(activeLocale);
  if (typeof window !== "undefined" && typeof window.dispatchEvent === "function") {
    window.dispatchEvent(new CustomEvent("abyssal:language-changed", { detail: { lang: activeLocale } }));
  }
}

let initialized = false;
function initI18n() {
  if (typeof window === "undefined" || typeof window.addEventListener !== "function" || initialized) return;
  initialized = true;

  applyLanguage(activeLocale);
  const toggle = document.querySelector("#lang-toggle");
  toggle?.addEventListener("click", () => {
    setLanguage(activeLocale === "ko" ? "en" : "ko");
  });

  window.addEventListener("storage", (event) => {
    if (event.key === STORAGE_KEY) {
      const newValue = event.newValue;
      const newLang = (newValue === "en" || newValue === "ko") ? newValue : DEFAULT_LANG;
      if (newLang !== activeLocale) {
        activeLocale = newLang;
        applyLanguage(activeLocale);
        if (typeof window.dispatchEvent === "function") {
          window.dispatchEvent(new CustomEvent("abyssal:language-changed", { detail: { lang: activeLocale } }));
        }
      }
    }
  });
}

if (typeof document !== "undefined") initI18n();

export { applyLanguage, setLanguage, currentLang, translate, translations };
